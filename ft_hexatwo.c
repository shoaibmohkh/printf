#include "ft_printf.h"

int	ft_hexatwo(unsigned int h)
{
	int		i;
	char	*num;

	num = "0123456789ABCDEF";
	i = 0;
	if (h >= 16)
		i += ft_hexatwo(h / 16);
	ft_putchar(num[h % 16]);
	return (i + 1);
}
